package javaMath;

public class JavaMath {

	public static void main(String[] args) {

		int x = 2;
		int y = 9;
		
		// Some of functions of Math Class
		
		System.out.println("Max (" + x + "," + y + ") = " + Math.max(x, y));

		System.out.println("Min (" + x + "," + y + ") = " + Math.min(x, y));

		System.out.println("Pow (" + x + "," + y + ") = " + Math.pow(x, y));

		System.out.println("Sin (" + x + ") = " + Math.sin(x));

		System.out.println("Cos (" + y + ") = " + Math.cos(y));

	}

}
